package basicJava;

public class MathOperations {

	public static void main(String[] args) {
		int x = 20;
		int y = 2;
		int sum = x + y;
		int mul = x * y;
		int div = x / y;
		int sub = x - y;
		System.out.println("Sum is: " + sum + " ,Mul is: " + mul + " ,Div is: " + div + " ,Sub is: " + sub);

	}

}
