package basicJava;

public class ConditionalStatement {

	public static void main(String[] args) {
		//We have 4 types of if-else
		// if statement 
		// if-else 
		// if-else-if 
		// if-else ladder
		int x = 20;
		int y = 30;
		int sum = x + y;
		if (sum == 50) {
			System.out.println("Equal");
		} else {
			System.out.println("Not Equal");
		}
	}

}
